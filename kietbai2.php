<?php
$a = 10; // Gán giá trị cho biến $a
$b = 5;  // Gán giá trị cho biến $b

$tong = $a + $b; // Tính tổng của $a và $b
$hieu = $a - $b; // Tính hiệu của $a và $b
$tich = $a * $b; // Tính tích của $a và $b
$thuong = $a / $b; // Tính thương của $a và $b
$du = $a % $b; // Tính số dư của phép chia $a cho $b

echo "Tổng 2 số $a và $b là: $tong<br>";    // In kết quả tổng
echo "Hiệu 2 số $a và $b là: $hieu<br>";    // In kết quả hiệu
echo "Tích 2 số $a và $b là: $tich<br>";    // In kết quả tích
echo "Thương 2 số $a và $b là: $thuong<br>";// In kết quả thương
echo "Số dư của phép chia trên là: $du<br>"; // In kết quả số dư
?>
